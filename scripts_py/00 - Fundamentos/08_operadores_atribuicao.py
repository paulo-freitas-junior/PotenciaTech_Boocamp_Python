# Atribuição Simples
saldo = 500
print(f"Atribuição simples", saldo)

# Atribuição com adição
saldo = 500
saldo += 200   # Igual a:  saldo = (saldo + 200)
print(f"Atribuição com adição", saldo)

# Atribuição com subtração
saldo = 500
saldo -= 200
print(f"Atribuição com subtração", saldo)

# Atribuição com multiplicação
saldo = 500
saldo *= 2
print(f"Atribuição com multiplação", saldo)

# Atribuição com divisão
saldo = 500
saldo /= 5
print(f"Atribuição com divisão", saldo)

# Atribuição com divisão inteira
saldo = 500
saldo //= 5
print(f"Atribuição com inteiro da divisão", saldo)

# Atribuição com Módulo
saldo = 500
saldo %= 480
print(f"Atribuição com Módulo", saldo)

# Atribuição com exponenciação
saldo = 80
saldo **=2
print(f"Atribuição com exponenciação", saldo)